package com.github.jscookie.javacookie.test.integration.test.utils;

public class Result {
  private String name;
  private String value;
  public Result( String name, String value ) {
    this.name = name;
    this.value = value;
  }
  public String getName() {
    return name;
  }
  public String getValue() {
    return value;
  }
}
